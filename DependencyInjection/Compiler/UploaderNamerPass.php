namespace Klipper\Bundle\ContentBundle\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;
use Symfony\Component\DependencyInjection\Compiler\PriorityTaggedServiceTrait;
use Symfony\Component\DependencyInjection\ContainerBuilder;

/**
 * @author François Pluchino <francois.pluchino@klipper.dev>
 */
class UploaderNamerPass implements CompilerPassInterface
{
    use PriorityTaggedServiceTrait;

    /**
     * {@inheritdoc}
     */
    public function process(ContainerBuilder $container): void
    {
        if (!$container->hasDefinition('klipper_content.uploader.namer_manager')) {
            return;
        }

        $container->getDefinition('klipper_content.uploader.namer_manager')
            ->replaceArgument(0, $this->findAndSortTaggedServices('klipper_content.uploader.namer', $container))
        ;
    }
}
